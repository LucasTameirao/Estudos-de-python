# estudo sobre criações de GUIs em python:

import tkinter as tk

root = tk.Tk() # Tk() atribui propriedades da biblioteca à variável

root.title('Minha janela') # title() define o título da janela

root.geometry('500x500') #geometry() define o tamanho da janela

root.mainloop() # mainloop() mantém a janela do tkinter aberta